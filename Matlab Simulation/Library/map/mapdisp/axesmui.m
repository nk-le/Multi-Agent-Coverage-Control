%--------------------------------------------------------------------------

function wrapped = wrapstr(str,maxlen)

w = cell(ceil(1.2*length(str)/maxlen),1);
last = 1;
spaces = [find(str == ' ') numel(str)];
k = 1;
while(last < numel(str))
    t = spaces(spaces < (maxlen + last-1));
    w{k} = str(last:t(end));
    last = t(end) + 1;
    k = k + 1;
end
w(k+1:end) = [];
wrapped = sprintf('%s\n',w{:});
wrapped(end) = [];

